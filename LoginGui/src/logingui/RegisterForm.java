    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JOptionPane.showMessageDialog(rootPane,"Confirm to insert those Data");
        try 
        {
            String firstName=jTextField1.getText();
            String lastName=jTextField2.getText();
            String Email=jTextField3.getText();
            String phoneNo=jTextField4.getText();
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            Connection con=DriverManager.getConnection("jdbc:derby://localhost:1527/Register","Register","demo");
            PreparedStatement ps=con.prepareStatement("insert into Register values(?,?,?,?)");
            ps.setString(1,firstName);
            ps.setString(2, lastName);
            ps.setString(3, Email);
            ps.setString(4, phoneNo);
            ps.executeUpdate();
            System.out.println("data inserted successfully");
            dispose();
            GettingInformation GI=new GettingInformation();
            GI.setVisible(true);
        }
        catch (Exception e)
        {
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

